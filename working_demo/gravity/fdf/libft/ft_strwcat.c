#include "includes/ft_printf.h"

char	*ft_strwcat(char *s1, const wchar_t wc)
{
	int		i;

	i = 0;
	if (!s1 || !wc)
		return (NULL);
	while (s1[i] != '\0')
		i++;
	s1[i++] = wc;
	s1[i] = '\0';
	return (s1);
}
