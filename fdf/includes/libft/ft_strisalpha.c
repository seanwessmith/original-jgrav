#include "includes/ft_printf.h"

int		ft_strisalpha(char *str)
{
	while (*str != '\0')
	{
		if ((*str <= 65 || *str >= 90) && (*str <= 97 || *str >= 122))
			return (0);
		str++;
	}
	return (1);
}
